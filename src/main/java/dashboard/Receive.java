    private void copyToClipboard(String text) {
    StringSelection selection = new StringSelection(text);
    Clipboard clipboard = Toolkit.getDefaultToolkit().getSystemClipboard();
    clipboard.setContents(selection, null);
}
    private void isiWalletData() {
        try {
            String userId = SupabaseUsers.getCurrentUserId(); // Ambil user_id global
            if (userId != null && !userId.isEmpty()) {
                JSONObject wallet = SupabaseWallet.getWalletByUserId(userId);
                if (wallet != null) {
                    // Ambil data wallet address dan balance
                    String walletAddress = wallet.getString("wallet_address");
                    double balance = wallet.getDouble("balance");

                    // Tampilkan ke input dan label
                    walletAddressField.setText(walletAddress);
                    walletAddressField.setEditable(false); // tidak bisa diketik
                    walletAddressField.setEnabled(false);  // terlihat disabled

                    balanceLabel.setText("" + balance);
                } else {
                    JOptionPane.showMessageDialog(this, "❌ Wallet tidak ditemukan.", "Error", JOptionPane.ERROR_MESSAGE);
                }
            } else {
                JOptionPane.showMessageDialog(this, "❌ User ID belum tersedia.", "Error", JOptionPane.ERROR_MESSAGE);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "❌ Gagal mengambil data wallet: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
        }

}

