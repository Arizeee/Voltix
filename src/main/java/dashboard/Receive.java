    private void copyButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_copyButtonActionPerformed
     try {
        String userId = SupabaseUsers.getCurrentUserId(); // Ambil user_id
        if (userId == null || userId.isEmpty()) {
            JOptionPane.showMessageDialog(this, "User ID tidak ditemukan. Silakan login ulang.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        JSONObject wallet = SupabaseWallet.getWalletByUserId(userId);
        if (wallet == null) {
            JOptionPane.showMessageDialog(this, "Wallet tidak ditemukan.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        String walletAddress = wallet.getString("wallet_address");
        copyToClipboard(walletAddress);

        JOptionPane.showMessageDialog(this, "Wallet Address berhasil disalin ke clipboard!", "Success", JOptionPane.INFORMATION_MESSAGE);
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Gagal menyalin wallet address: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        e.printStackTrace();
    }
    }//GEN-LAST:event_copyButtonActionPerformed

